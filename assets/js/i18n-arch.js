  function merge() {
    const api = window.SemeAI_I18n;
    if (!api || !api.dict) return false;
    ["en", "uk", "ru"].forEach((lang) => {
      if (!api.dict[lang]) api.dict[lang] = {};
      Object.assign(api.dict[lang], arch[lang] || {});
    });
    return true;
  }

  function boot() {
    if (!merge()) return;
    try {
      apiApply();
    } catch {}
  }

  function apiApply() {
    const api = window.SemeAI_I18n;
    if (api && typeof api.apply === "function") api.apply(document);
  }

  window.SemeAI_I18nArch = { dict: arch, merge, apply: apiApply };

  if (window.SemeAI_I18n) {
    merge();
    if (document.readyState === "loading") {
      document.addEventListener("DOMContentLoaded", () => apiApply(), { once: true });
    } else {
      apiApply();
    }
  } else {
    document.addEventListener("DOMContentLoaded", boot, { once: true });
  }
